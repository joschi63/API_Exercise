app = FastAPI()


def verify_password(hashed_password, plain_password): 
    try: 
        return password_hash.verify(hashed_password, plain_password)
    except VerifyMismatchError: #to avoid that argon2 throws his own error
        return False


def create_access_token(data: dict, expires_delta: timedelta | None = None):
    to_encode = data.copy()
    expires_delta = timedelta(minutes=ACCESS_TOKEN_EXPIRE_MINUTES)
    if expires_delta: 
        expire = datetime.now(timezone.utc) + expires_delta
    else:
        expire = datetime.now(timezone.utc) + timedelta(minutes=15)
    
    to_encode.update({"exp": expire})
    encoded_jwt = jwt.encode(to_encode, SECRET_KEY, algorithm=ALGORITHM)
    return encoded_jwt


def verify_access_token(token: str, credentials_exception):
    try: 
        payload = jwt.decode(
            token,
            SECRET_KEY,
            algorithms=[ALGORITHM]
        )

        id = payload.get("user_id")
    
        token_data = TokenData(id=id)
    
        if id is None:
            raise credentials_exception
        token_data = TokenData(id=id)
    
    except InvalidTokenError:
        raise credentials_exception
    
    return token_data
    
def get_current_user(token: Annotated[str, Depends(oauth2_scheme)], session: SessionDep):
    credentials_exception = HTTPException(
        status_code=status.HTTP_401_UNAUTHORIZED,
        detail="Could not validate credentials",
        headers={"WWW-Authenticate": "Bearer"},
    )

    token_user = verify_access_token(token, credentials_exception)

    session_user = session.get(user.User, token_user.id)

    return session_user



